// 메인 분석 함수
// ─────────────────────────────────────────────
export function analyzeScores(scores) {
  // 1. 신뢰도
  const lScore = scores["L"] ?? 3;
  const reliabilityPct = Math.max(0, Math.min(100, ((5 - lScore) / 4) * 100));
  const reliabilityLevel =
    reliabilityPct >= 70 ? "high" : reliabilityPct >= 40 ? "medium" : "low";
  const reliabilityComment =
    reliabilityLevel === "high"
      ? "응답 패턴이 일관되고 솔직합니다. 신뢰도 높은 결과입니다."
      : reliabilityLevel === "medium"
      ? "대체로 솔직한 응답이나, 일부 사회적으로 바람직한 방향으로 응답된 문항이 감지됩니다."
      : "긍정적 이미지를 위해 응답을 조정한 흔적이 있습니다. 결과 해석 시 참고하세요.";

  // 2. 주요 차원 점수 순위 정렬 (L 제외)
  const mainDims = ["E", "C", "A", "O", "N", "S", "R", "T", "P"];
  const ranked = mainDims
    .filter((d) => scores[d] != null)
    .sort((a, b) => (scores[b] ?? 0) - (scores[a] ?? 0));

  // 3. 성격 유형
  const typeInfo = TYPE_MAP.find((t) => t.match(ranked)) || TYPE_MAP[TYPE_MAP.length - 1];

  // 4. 강점: 상위 3개 차원
  const strengths = ranked.slice(0, 3).map((d) => HIGH[d]);

  // 5. 개선 영역: 하위 3개 차원
  const weaknesses = ranked.slice(-3).map((d) => LOW[d]);

  return {
    reliabilityLevel,
    reliabilityComment,
    personalityType: typeInfo.name,
    typeDescription: typeInfo.desc,
    strengths,
    weaknesses,
    careerFit: typeInfo.career,
    advice: typeInfo.advice,
  };
}
